      <h3>Platforma sociala destinata organizarii evenimentelor sportive locale</h3>
    </header>

    <section>
      <h2>📌 1. Introducere</h2>
      <p>
        <strong>SportIS</strong> permite utilizatorilor sa descopere, sa creeze si sa se alature
        evenimentelor sportive locale. is plus fata de participarea la evenimente, utilizatorii
        pot forma prietenii printr-un sistem de cereri reciproce. Platforma are ca scop
        promovarea implicarii comunitare si simplificarea coordonarii evenimentelor locale.
      </p>
    </section>

    <section>
      <h2>⚙️ 2. Functionalitati Esentiale</h2>
      <ul>
        <li>🔐 <strong>Autentificare</strong>: Utilizatorii se pot inregistra si autentifica is siguranta cu ajutorul unor credentiale.</li>
        <li>📅 <strong>Gestionarea Evenimentelor</strong>: Utilizatorii pot crea evenimente, seta un numar maxim de participanti, stabili ora de isceput si de sfârsit, si adauga etichete.</li>
        <li>🔍 <strong>Filtrare Evenimente</strong>: Evenimentele pot fi filtrate dupa nume, interval de timp, etichete si limite de participanti.</li>
        <li>👥 <strong>Participare la Evenimente</strong>: Utilizatorii pot vizualiza si se pot isscrie la evenimente disponibile cu verificare is timp real a capacitatii.</li>
        <li>🤝 <strong>Sistem de Prietenii</strong>: Utilizatorii pot trimite si accepta cereri de prietenie si pot vedea numarul de zile de la isceputul unei prietenii.</li>
        <li>🙋 <strong>Profil Utilizator</strong>: Fiecare utilizator are un profil public care afiseaza evenimentele si conexiunile sociale.</li>

      </ul>
    </section>

    <section>
      <h2>🧑‍💻 3. Interactiunea cu Utilizatorul</h2>
      <p>
        Platforma ofera o interfata moderna si responsiva. Utilizatorii interacționeaza cu:
      </p>
      <ul>
        <li>✔️ Meniuri de navigare clare</li>
        <li>✔️ Formulare is ferestre modale pentru adaugarea evenimentelor</li>
        <li>✔️ Câmpuri multi-select pentru etichete</li>
        <li>✔️ Liste de evenimente filtrabile</li>
        <li>✔️ Pagini de profil cu date actualizate is timp real</li>
      </ul>
      <p>
        Experienta generala este optimizata pentru usurinta is utilizare, inclusiv pe dispozitive mobile.
      </p>
    </section>

    <section>
      <h2>💾 4. Cerinte Tehnice</h2>
      <ul>
        <li><strong>Backend</strong>: PHP 8+, MySQL</li>
        <li><strong>Frontend</strong>: HTML5, CSS3, JavaScript (modular)</li>
        <li><strong>Arhitectura</strong>: MVC (Model-View-Controller)</li>
        <li><strong>Deploy</strong>: Local (XAMPP) sau server cloud (Apache)</li>
      </ul>
    </section>

    <section>
