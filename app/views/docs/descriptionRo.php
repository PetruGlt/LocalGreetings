        <li>👤 Doar utilizatorii autentificati pot crea sau se pot alatura la evenimente.</li>
        <li>📱 Interfata este optimizata pentru browsere moderne (Chrome, Firefox, Edge).</li>
        <li>🛠️ Gestionarea etichetelor este predefinita sau se face dinamic la nevoie.</li>
      </ul>
    </section>

